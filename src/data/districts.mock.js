function buildSquarePolygon(center, delta = 0.035) {
  const { lat, lng } = center;
  return [
    [
      [lng - delta, lat - delta],
      [lng + delta, lat - delta],
      [lng + delta, lat + delta],
      [lng - delta, lat + delta],
      [lng - delta, lat - delta],
    ],
  ];
}

